from __future__ import annotations

import inspect

from collections.abc import Callable
from dataclasses import dataclass, replace
from typing import Any, TypeVar


@dataclass
class BenchmarkInfo:
    """Information about a Callable for :py:meth:`BenchmarkHarness.run`."""

    name: str
    input_names: dict[str, str]
    output_names: str | tuple[str, ...]
    formats: dict[str, Callable[..., str]]
    returns_time: int

    def replace(self, /, **changes: Any) -> BenchmarkInfo:
        return replace(self, **changes)


_T = TypeVar("_T")


_INFO: str = "__cpn_benchmark_harness_info"


_TIME: str = "time (milliseconds)"


def _create_benchmark_info(
    f: Callable[..., Any],
    name: str | None = None,
    input_names: dict[str, str] = {},
    output_names: str | tuple[str, ...] | None = None,
    formats: dict[str, Callable[..., str]] = {},
    returns_time: int = 0,
) -> BenchmarkInfo:
    this_name = name
    if this_name is None:
        members = inspect.getmembers(f)
        for key, value in members:
            if key == "__name__":
                this_name = value
    if this_name is None:
        raise RuntimeError(f"{f} has no __name__, a name must be provided")
    if returns_time == 0 and output_names is None:
        output_names = _TIME
    if output_names is None:
        raise RuntimeError(
            f"Unable to determine output_names for {this_name}, use @benchmark_info()"
        )
    assert output_names is not None
    sig = inspect.signature(f)
    this_input_names = {}
    this_formats = {}
    if "np" in sig.parameters:
        this_input_names = {"np": "array package"}
        this_formats = {"array package": lambda np: np.__name__}
    this_input_names.update(input_names)
    this_formats.update(formats)
    for param_name in this_input_names:
        if param_name not in sig.parameters:
            raise RuntimeError(f"{param_name} is not a parameter of {f}")
        param = sig.parameters[param_name]
        if param.kind not in [
            param.POSITIONAL_ONLY,
            param.POSITIONAL_OR_KEYWORD,
        ]:
            raise RuntimeError(
                f"Only positional arguments get columns, {param_name} is not positional"
            )
    info = BenchmarkInfo(
        this_name, this_input_names, output_names, this_formats, returns_time
    )
    return info


def benchmark_info(
    *,
    name: str | None = None,
    input_names: dict[str, str] = {},
    output_names: str | tuple[str, ...] | None = None,
    formats: dict[str, Callable[..., str]] = {},
    returns_time: bool | int = True,
) -> Callable[..., Any]:
    """Decorator for setting the benchmark name and column names
    generated by :py:meth:`BenchmarkHarness.run`.

    By default the names of positional parameters in the signature of a
    function will be the names of columns in the benchmark log, with one
    exception: a positional parameter named ``np`` is assumed to be
    an array package and will get the column name ``'array package'``.
    You can override this behavior by including ``'np': 'a different name'``
    in ``input_names``.

    Parameters
    ----------
    name: str | None = None
        Name given to :py:func:`legate.util.benchmark.benchmark_log` for this
        function.
    input_names: dict[str, str] = {}
        Give optional column names for inputs to use instead of parameter
        names. (See note about special handling for ``np`` above.)
    output_names: dict[str, str] = {}
        Give names for colums generated from output values.
    formats: dict[str, Collable[..., str]] = {}
        Optional mapping from a column name to a format
        function that will be applied before the data row is added.
    returns_time: bool | int = False
        Whether the function returns a time that can be used by
        :py:meth:`BenchmarkHarness.run_timed`.  If ``False``,
        it does not; if ``True``, the function returns the time as its only
        argument.  If an integer ``k`` is given, the function returns a tuple
        and the time is the ``k``th return argument
    """

    this_returns_time: int
    if isinstance(returns_time, bool):
        this_returns_time = 0 if returns_time else -1
    else:
        this_returns_time = returns_time

    def inner(f: Callable[..., _T]) -> Callable[..., _T]:
        info = _create_benchmark_info(
            f, name, input_names, output_names, formats, this_returns_time
        )
        setattr(f, _INFO, info)
        return f

    return inner


def get_benchmark_info(f: Callable[..., Any]) -> BenchmarkInfo:
    """Get the :py:class:`BenchmarkInfo` of a Callable.

    Parameters
    ----------
    f: Callable[...,Any]
        A callable.  If it was decorated with :py:func:`benchmark_info`,
        that information will be returned; otherwise default information
        will be generated.

    Returns
    -------
    BenchmarkInfo
    """
    if hasattr(f, _INFO):
        v = getattr(f, _INFO)
        assert isinstance(v, BenchmarkInfo)
        return v
    return _create_benchmark_info(f)
