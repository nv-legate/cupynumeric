"""
Shared benchmark utilities for microbenchmark suite.

This module provides a lightweight coordination layer for running multiple
benchmarks within a suite using the standard benchmark.py infrastructure.

MicrobenchmarkSuite: Coordinates benchmark execution
   - Each benchmark uses run_benchmark() to create separate CSV files
   - Tracks benchmark count for suite summary

Design:
- Each benchmark calls run_timed() which creates a separate CSV file
- This preserves compatibility with existing visualization tools
- Optional unified summary table can be generated at the end
"""

from __future__ import annotations

from sys import stdout

from argparse import ArgumentParser, Namespace
from dataclasses import dataclass
from typing import Any, Callable

from .harness import ArrayPackage, BenchmarkHarness, BenchmarkHarnessConfig
from .info import BenchmarkInfo, get_benchmark_info
from .use_rich import use_rich, HAVE_RICH


@dataclass
class MicrobenchmarkConfig:
    harness_config: BenchmarkHarnessConfig
    runs: int
    warmup: int

    def print_panel(self, lines: list[str], title: str = "") -> None:
        if HAVE_RICH and use_rich(
            stdout,
            start_runtime=self.harness_config.package == ArrayPackage.LEGATE,
        ):
            from rich.console import Console
            from rich.panel import Panel

            console = Console(file=stdout)
            console.print(
                Panel.fit(
                    "\n".join(lines),
                    title=f"[bold]{title}[/bold]" if title else None,
                )
            )
        else:
            print("=" * 80)
            if title:
                print(title)
            print("-" * 80)
            for line in lines:
                print(line)
            print("=" * 80)


class MicrobenchmarkHarness(BenchmarkHarness):
    """
    Handles common behavior of all microbenchmark suites.

    When adding to the microbenchmarks, Do not subclass directly: use
    :py:class:`MicrobenchmarkSuite`.
    """

    name: str = "microbenchmark harness"

    @classmethod
    def add_parser_group(cls, parser: ArgumentParser) -> Any:
        group = super(MicrobenchmarkHarness, cls).add_parser_group(parser)
        group.add_argument(
            "--runs",
            dest="__cpn_runs",
            metavar="RUNS",
            type=int,
            default=5,
            help="Number of timing runs per benchmark (default: 5)",
        )
        group.add_argument(
            "--warmup",
            dest="__cpn_warmup",
            metavar="WARMUP",
            type=int,
            default=2,
            help="Number of warmup runs (default: 2)",
        )
        return group

    @staticmethod
    def config(
        args: Namespace, harness_config: BenchmarkHarnessConfig | None = None
    ) -> MicrobenchmarkConfig:
        if harness_config is None:
            harness_config = BenchmarkHarness.config_from_namespace(args)
        vargs = vars(args)
        return MicrobenchmarkConfig(
            harness_config,
            runs=int(vargs["__cpn_runs"]),
            warmup=int(vargs["__cpn_warmup"]),
        )

    def __init__(self, config: MicrobenchmarkConfig):
        """
        Initialize microbenchmark suite coordinator.

        Parameters
        ----------
        config: MicrobenchmarkConfig
            configuration generated by :py:meth:`config`.
        """
        super().__init__(config.harness_config)
        self._suite_config: MicrobenchmarkConfig = config
        self.benchmark_count: int = 0
        self._benchmark_names: list[
            str
        ] = []  # Track benchmark names for summary

    @property
    def runs(self) -> int:
        return self._suite_config.runs

    @property
    def warmup(self) -> int:
        return self._suite_config.warmup

    def run_with_info(
        self,
        info: BenchmarkInfo,
        f: Callable[..., Any],
        *args: Any,
        **kwargs: Any,
    ) -> None:
        name = info.name
        full_name = f"{self.name}_{name}"
        info = info.replace(name=full_name)
        super().run_with_info(info, f, *args, **kwargs)
        self.benchmark_count += 1
        self._benchmark_names.append(full_name)

    def run_timed_with_info(
        self,
        info: BenchmarkInfo,
        f: Callable[..., Any],
        *args: Any,
        **kwargs: Any,
    ) -> None:
        name = info.name
        full_name = f"{self.name}_{name}"
        info = info.replace(name=full_name)
        super().run_timed_with_info(info, f, *args, **kwargs)
        self.benchmark_count += 1
        self._benchmark_names.append(full_name)

    def run(self, f: Callable[..., Any], *args: Any, **kwargs: Any) -> None:
        self.run_with_info(get_benchmark_info(f), f, *args, **kwargs)

    def run_timed(
        self, f: Callable[..., Any], *args: Any, **kwargs: Any
    ) -> None:
        self.run_timed_with_info(get_benchmark_info(f), f, *args, **kwargs)


class MicrobenchmarkSuite(MicrobenchmarkHarness):
    """
    Base class for microbenchmark suites.
    """

    name: str = "microbenchmark suite base"

    @staticmethod
    def add_suite_parser_group(parser: ArgumentParser) -> None:
        pass

    def __init__(self, config: MicrobenchmarkConfig, _: Namespace) -> None:
        super().__init__(config)

    def print_panel(self, lines: list[str], title: str = "") -> None:
        self._suite_config.print_panel(lines, title)

    def get_benchmark_names(self) -> list[str]:
        """Get list of all benchmark names run by this suite."""
        return self._benchmark_names

    def print_suite_summary(self) -> None:
        """Print suite-level summary."""
        if self.benchmark_count > 0:
            msg = [f"Total benchmarks run: {self.benchmark_count}"]
            self.print_panel(msg, f"SUITE COMPLETE: {self.name}")

    def print_config(self) -> None:
        pass

    def run_suite(self, size: int) -> None:
        pass

    def __enter__(self) -> MicrobenchmarkSuite:
        self.print_config()
        s = super().__enter__()
        assert isinstance(s, MicrobenchmarkSuite)
        return s

    def __exit__(self, a: Any, b: Any, c: Any) -> None:
        self.print_suite_summary()
        super().__exit__(a, b, c)
